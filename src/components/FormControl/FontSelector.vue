<template>
  <div class="wrapper">
    <el-select placeholder="请选择相关字体库" clearable v-bind="$attrs">
      <el-option
        v-for="item in fontList"
        :key="item.cn"
        :value="item.en"
        :label="item.cn"
        :style="{ fontFamily: item.en }"
      >
        <span style="float: left; font-weight: bold">{{ item.cn }}</span>
        <span style="float: right; color: #8492a6; font-size: 13px; margin-left: 36px">{{
          item.en
        }}</span>
      </el-option>
    </el-select>
    <div v-if="showRefresh" :class="['icon-refresh', rotate && 'rotate']" @click="refresh">
      <Icon name="refresh" size="1em" />
    </div>
  </div>
</template>

<script lang="ts">
import { computed, defineComponent, onMounted, ref } from 'vue'
import { useStore } from '@/store'
export default defineComponent({
  name: 'FontSelector',
  props: {
    showRefresh: {
      type: Boolean,
      default: false
    }
  },
  setup() {
    const store = useStore()
    onMounted(() => {
      if (!store.fontFamilyList || store.fontFamilyList.length === 0) {
        store.updateFontFamilyList()
      }
    })
    const fontList = computed(() => store.fontFamilyList)
    const rotate = ref(false)
    const refresh = () => {
      store.updateFontFamilyList()
      rotate.value = true
      setTimeout(() => {
        rotate.value = false
      }, 500)
    }
    return {
      fontList,
      rotate,
      refresh
    }
  }
})
</script>
<style lang="scss" scoped>
.wrapper {
  display: inline-flex;
  align-items: center;
  .icon-refresh {
    display: inline-flex;
    align-items: center;
    font-size: 20px;
    color: #aaa2b3;
    cursor: pointer;
    margin-left: 8px;
    &.rotate {
      transform: rotate(360deg);
      transition: transform 0.4s ease-in-out;
    }
  }
}
</style>
